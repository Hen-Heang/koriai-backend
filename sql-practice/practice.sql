-- PART 1: SCHEMA (trimmed from db/migration/V1, V19, V24, V28)
-- ============================================================================

DROP TABLE IF EXISTS tasks, goal_stars, goals, sentence_corrections, users CASCADE;

CREATE EXTENSION IF NOT EXISTS pgcrypto; -- gives us gen_random_uuid()

CREATE TABLE users (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email                   VARCHAR(255) NOT NULL UNIQUE,
    display_name            VARCHAR(100) NOT NULL,
    korean_level            VARCHAR(20)  NOT NULL DEFAULT 'BEGINNER',
    timezone                TEXT         NOT NULL DEFAULT 'UTC',
    reminder_offset_minutes INTEGER      NOT NULL DEFAULT 30,
    created_at              TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);

CREATE TABLE sentence_corrections (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id        BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    original_text  TEXT NOT NULL,
    corrected_text TEXT NOT NULL,
    grammar_points JSONB,            -- e.g. ["past tense", "honorifics"]
    repetitions    INTEGER NOT NULL DEFAULT 0,
    created_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE goals (
    id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id     BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    title       TEXT NOT NULL,
    status      TEXT NOT NULL DEFAULT 'active',
    is_public   BOOLEAN NOT NULL DEFAULT FALSE,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE goal_stars (
    user_id    BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    goal_id    UUID   NOT NULL REFERENCES goals (id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    PRIMARY KEY (user_id, goal_id)
);

CREATE TABLE tasks (
    id               UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    goal_id          UUID REFERENCES goals (id) ON DELETE CASCADE,
    user_id          BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    title            TEXT NOT NULL,
    completed        BOOLEAN NOT NULL DEFAULT FALSE,
    start_date       TIMESTAMPTZ,
    end_date         TIMESTAMPTZ,
    daily_start_time TIME,
    daily_end_time   TIME,
    tags             TEXT[] NOT NULL DEFAULT '{}',
    reminder_sent_at TIMESTAMPTZ,
    due_soon_sent_at TIMESTAMPTZ,
    overdue_sent_at  TIMESTAMPTZ,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);


