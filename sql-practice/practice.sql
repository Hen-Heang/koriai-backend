ON CONFLICT (user_id, goal_id) DO NOTHING;          -- run this twice; 2nd run inserts 0 rows

-- 3.3 JSONB read/write — like SentenceCorrectionMapper -----------------------
SELECT id, original_text, grammar_points, grammar_points::text AS grammar_points_as_text
FROM sentence_corrections
WHERE user_id = 1;

-- pull one element out of the JSON array:
SELECT id, grammar_points -> 0 AS first_point        -- -> keeps JSON type
FROM sentence_corrections
WHERE jsonb_array_length(grammar_points) > 0;

-- 3.4 Array column — like tasks.tags (TaskMapper) ---------------------------
SELECT id, title, tags FROM tasks WHERE 'vocab' = ANY(tags);   -- array membership test
SELECT id, title, tags FROM tasks WHERE tags && ARRAY['grammar','listening']; -- "overlaps"

-- 3.5 EXISTS subquery — like GoalStarMapper.existsStar -----------------------
SELECT CASE WHEN EXISTS (
    SELECT 1 FROM goal_stars WHERE user_id = 1 AND goal_id = '33333333-3333-3333-3333-333333333333'
) THEN 1 ELSE 0 END AS starred;

-- 3.6 Correlated subquery enrichment — like GoalMapper.findAccessibleByUser
SELECT
    g.id, g.title,
    (SELECT COUNT(*) FROM tasks t WHERE t.goal_id = g.id)               AS task_total,
    (SELECT COUNT(*) FROM tasks t WHERE t.goal_id = g.id AND t.completed) AS task_completed,
    EXISTS (SELECT 1 FROM goal_stars s WHERE s.goal_id = g.id AND s.user_id = 1) AS starred
FROM goals g
WHERE g.user_id = 1
ORDER BY g.created_at DESC;

-- 3.7 CTE + timezone math — like TaskMapper.findDueReminders ----------------
WITH computed AS (
    SELECT
        t.id AS task_id,
        t.title,
        u.reminder_offset_minutes AS offset_min,
        (
            (date(t.start_date AT TIME ZONE COALESCE(u.timezone, 'UTC')) + t.daily_start_time)
                AT TIME ZONE COALESCE(u.timezone, 'UTC')
        ) AS start_instant
    FROM tasks t
    JOIN users u ON u.id = t.user_id
    WHERE t.completed = false
      AND t.daily_start_time IS NOT NULL
      AND t.reminder_sent_at IS NULL
)
SELECT task_id, title, start_instant, offset_min
FROM computed
WHERE start_instant > now()
  AND (start_instant - make_interval(mins => offset_min)) <= now();
