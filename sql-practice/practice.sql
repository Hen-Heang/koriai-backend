-- 3.1 Plain CRUD — like AchievementMapper / TelegramLinkMapper -------------
SELECT id, email, display_name, korean_level FROM users ORDER BY id;

-- 3.2 Upsert ("INSERT ... ON CONFLICT") — like FoundationProgressMapper.upsert
